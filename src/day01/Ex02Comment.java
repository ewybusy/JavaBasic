package day01;
// 주석(Comment)
// 주석이란, 해당 클래스 파일에 적는 설명으로써,
// 실제 코드가 아니기 때문에
// 우리가 원하는대로 적어주어도 된다.

// 주석에는 크게 두가지 종류가 잇는데
// 한줄 주석과 여러줄 주석이다.
// 한줄 주석은 // 가 나오는 다음칸 부터 엔터를 치기 전까지를 주석으로 만든다.
// 여러줄 주석은 /*로 시작해서 */가 나올때까지의 모든 줄을 주석으로 만든다.


public class Ex02Comment {
	public static void main(String[] args) {
		System.out.println("1");
		System.out.println("2");
		/*System.out.println("3");
		System.out.println("4");
		System.out.println("5");
		*/
	}
}
