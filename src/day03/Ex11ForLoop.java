package day03;
// for 반복문
// 회차와 상관없이 특정 조건을 만족할 때 계속 반복시키는 것이 목적이었던
// while 반복문과는 다르게
// for 반복문은 그 목적이 좀 더 반복횟수에 집중되어있는
// 반복문이다.
public class Ex11ForLoop {
	public static void main(String[] args) {
		// for 반복문은 주로 다음과 같은 구조를 가진다.
		// for(제어 변수의 선언과 초기화; 조건식; 제어 변수의 변화식) {
		// 	   반복할 코드
		// }
		
		for(int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		
		// for문은 여러분들이 나중에 배우게 될
		// 배열 혹은 특정 데이터타입의 값이 여러개가 모여있는
		// 콜렉션 등을 다룰 때 주로 사용이 된다.
		
	}
	
}
